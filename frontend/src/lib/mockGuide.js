export function generateMockGuide(userMessage) {
  const msg = userMessage.toLowerCase();
  let data = DEFAULT_GAME;

  for (const [key, value] of Object.entries(GAME_DATA)) {
    if (msg.includes(key)) {
      data = value;
      break;
    }
  }

  const gameName = data.name || extractGenericGameName(userMessage);

  return {
    game: gameName,
    icon: data.icon,
    difficulty: data.difficulty,
    time: data.time,
    steps: data.steps,
    tips: data.tips,
    sources: data.sources,
  };
}

function extractGenericGameName(message) {
  const words = message.split(" ").filter((w) => w.length > 2);
  return words.length > 0 ? words.slice(0, 4).join(" ") : "Il Tuo Gioco";
}